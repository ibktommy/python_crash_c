# Using Quotes
sentence1 = 'I told my friend, "Python is a great programming language."';
sentence2 = "The movie 'Dune' is such a great movie to watch."
print(sentence1)
print(sentence2)

# Changing Case
captalizeWord = 'hello'
upperCaseWord = 'hello'
lowerCaseWord = 'HELLO'
print(captalizeWord.title())
print(upperCaseWord.upper())
print(upperCaseWord.lower())

# COncatenating Strings
firstName = 'Billy'
lastName = 'Jean'
fullName = firstName + " " + lastName
print(fullName)

# Whitespaces and Newline
tab = 'Languages I work with are:\n\tPython\n\tJavascript\n\tJava'
print(tab)

# Remove Whitespaces
clearSpace = "  Python is a cool language to learn "
print(clearSpace.lstrip())
print(clearSpace.rstrip())
print(clearSpace.strip())



