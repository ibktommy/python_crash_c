####### Seeing the World Exercise #######
places = ['kuwait', 'singapore', 'mecca', 'russia', 'malaysia']

# print sorted list
print(sorted(places))

# print original list
print(places)

# print in reversed sorted
print(sorted(places, reverse=True))

# print original list
print(places)

# reverse list unsorted
places.reverse()
print(places)

# reverse list again
places.reverse()
print(places)

# sort list
places.sort()
print(places)

# sort list reversed
places.sort(reverse=True)
print(places)
print('\n')


