# Sort list Alphabetically
cars = ['tesla', 'bmw', 'kia', 'toyota']
cars.sort()
print(cars)

#Reverse Sorted
carsReversed = cars
carsReversed.sort(reverse=True)
print(carsReversed)

# Temporal Sorting
print(sorted(cars))

# List in Reverse Order
carsReversed.reverse()
print(carsReversed)

#Length of List
print(len(cars))
