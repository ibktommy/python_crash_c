##### Pie #####
pie = ['meat pie', 'chicken pie', 'fish pie']

# Print Pie
for eachPie in pie:
  print(eachPie.title())

print('\n')

# Print a message with each pie
for eachPie in pie:
  print("I like " + eachPie.title())
  print('\n')
print("I love pie so much ;)")


